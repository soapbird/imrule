#[test]
fn init_scaffolds_imrule_files_without_overwriting_existing_content() {
    let tmp = tempdir().unwrap();

    let init = Command::cargo_bin("imrule")
        .unwrap()
        .args(["init", "--project-root", tmp.path().to_str().unwrap()])
        .output()
        .unwrap();
    assert!(init.status.success());
    assert!(String::from_utf8_lossy(&init.stdout).contains("ImRule initialized"));

    let agents = tmp.path().join(".imrule/AGENTS.md");
    let toml = tmp.path().join(".imrule/imrule.toml");
    assert!(fs::read_to_string(&agents)
        .unwrap()
        .contains("Centralised AI agent instructions"));
    assert!(fs::read_to_string(&toml)
        .unwrap()
        .contains("default_agents"));

    fs::write(&agents, "custom").unwrap();
    Command::cargo_bin("imrule")
        .unwrap()
        .args(["init", "--project-root", tmp.path().to_str().unwrap()])
        .assert()
        .success();
    assert_eq!(fs::read_to_string(&agents).unwrap(), "custom");
}

