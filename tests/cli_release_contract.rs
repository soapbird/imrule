#[test]
fn apply_prefers_imrule_over_ruler_when_both_exist() {
    let tmp = tempdir().unwrap();
    fs::create_dir_all(tmp.path().join(".imrule")).unwrap();
    fs::create_dir_all(tmp.path().join(".ruler")).unwrap();
    fs::write(tmp.path().join(".imrule/AGENTS.md"), "New rules.").unwrap();
    fs::write(tmp.path().join(".ruler/AGENTS.md"), "Old rules.").unwrap();

    let output = Command::cargo_bin("imrule")
        .unwrap()
        .args([
            "apply",
            "--project-root",
            tmp.path().to_str().unwrap(),
            "--agents",
            "claude",
            "--no-mcp",
        ])
        .output()
        .unwrap();
    assert!(output.status.success());

    let claude = fs::read_to_string(tmp.path().join("CLAUDE.md")).unwrap();
    assert!(claude.contains("New rules."));
    assert!(!claude.contains("Old rules."));

    // No deprecation warning when .imrule exists.
    let stderr = String::from_utf8_lossy(&output.stderr);
    assert!(!stderr.contains("legacy"));
}
