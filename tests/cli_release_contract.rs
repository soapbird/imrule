#[test]
fn cli_version_and_help_are_release_ready() {
    let version = Command::cargo_bin("imrule")
        .unwrap()
        .arg("--version")
        .output()
        .unwrap();
    assert!(version.status.success());
    assert!(String::from_utf8_lossy(&version.stdout).contains("imrule 0.1.0"));

    let help = Command::cargo_bin("imrule")
        .unwrap()
        .arg("apply")
        .arg("--help")
        .output()
        .unwrap();
    assert!(help.status.success());
    let stdout = String::from_utf8_lossy(&help.stdout);
    assert!(stdout.contains("Apply imrule configurations to supported AI agents"));
    assert!(stdout.contains("--project-root"));
    assert!(stdout.contains("--dry-run"));
    assert!(stdout.contains("agentsmd, aider, amazonqcli"));
}

#[test]
fn apply_and_revert_are_native_rust_commands() {
    let tmp = tempdir().unwrap();
    fs::create_dir_all(tmp.path().join(".imrule")).unwrap();
    fs::write(tmp.path().join(".imrule/AGENTS.md"), "Always be concise.").unwrap();

    Command::cargo_bin("imrule")
        .unwrap()
        .args([
            "apply",
            "--project-root",
            tmp.path().to_str().unwrap(),
            "--agents",
            "claude,cline",
        ])
        .assert()
        .success();

    let claude = fs::read_to_string(tmp.path().join("CLAUDE.md")).unwrap();
    assert!(claude.starts_with("<!-- Generated by ImRule -->"));
    assert!(claude.contains("Always be concise."));
    assert!(tmp.path().join(".clinerules").is_file());

    Command::cargo_bin("imrule")
        .unwrap()
        .args([
            "revert",
            "--project-root",
            tmp.path().to_str().unwrap(),
            "--agents",
            "claude,cline",
        ])
        .assert()
        .success();

    assert!(!tmp.path().join("CLAUDE.md").exists());
    assert!(!tmp.path().join(".clinerules").exists());
}

