#[test]
fn init_scaffolds_imrule_files_without_overwriting_existing_content() {
    let tmp = tempdir().unwrap();

    let init = Command::cargo_bin("imrule")
        .unwrap()
        .args(["init", "--project-root", tmp.path().to_str().unwrap()])
        .output()
        .unwrap();
    assert!(init.status.success());
    assert!(String::from_utf8_lossy(&init.stdout).contains("ImRule initialized"));

    let agents = tmp.path().join(".imrule/AGENTS.md");
    let toml = tmp.path().join(".imrule/imrule.toml");
    assert!(fs::read_to_string(&agents)
        .unwrap()
        .contains("Centralised AI agent instructions"));
    assert!(fs::read_to_string(&toml)
        .unwrap()
        .contains("default_agents"));

    fs::write(&agents, "custom").unwrap();
    Command::cargo_bin("imrule")
        .unwrap()
        .args(["init", "--project-root", tmp.path().to_str().unwrap()])
        .assert()
        .success();
    assert_eq!(fs::read_to_string(&agents).unwrap(), "custom");
}

#[test]
fn init_global_uses_xdg_config_home() {
    let tmp = tempdir().unwrap();

    Command::cargo_bin("imrule")
        .unwrap()
        .env("XDG_CONFIG_HOME", tmp.path())
        .args(["init", "--global"])
        .assert()
        .success();

    assert!(tmp.path().join("imrule/AGENTS.md").is_file());
    assert!(tmp.path().join("imrule/imrule.toml").is_file());
}

#[test]
fn apply_works_with_legacy_ruler_directory() {
    let tmp = tempdir().unwrap();
    fs::create_dir_all(tmp.path().join(".ruler")).unwrap();
    fs::write(tmp.path().join(".ruler/AGENTS.md"), "Legacy ruler rules.").unwrap();

    let output = Command::cargo_bin("imrule")
        .unwrap()
        .args([
            "apply",
            "--project-root",
            tmp.path().to_str().unwrap(),
            "--agents",
            "claude",
            "--no-mcp",
        ])
        .output()
        .unwrap();
    assert!(output.status.success());

    let claude = fs::read_to_string(tmp.path().join("CLAUDE.md")).unwrap();
    assert!(claude.starts_with("<!-- Generated by ImRule -->"));
    assert!(claude.contains("Legacy ruler rules."));

    // Should print deprecation warning to stderr.
    let stderr = String::from_utf8_lossy(&output.stderr);
    assert!(stderr.contains("legacy '.ruler/' directory"));
}

