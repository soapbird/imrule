#[test]
fn concatenate_rules_matches_native_section_format() {
    let tmp = tempdir().unwrap();
    let base = tmp.path();
    let files = vec![
        (
            base.join(".ruler/AGENTS.md"),
            "\n# Root Rules\n\nPrimary rule.\n\n".to_string(),
        ),
        (
            base.join(".ruler/nested/extra.md"),
            "# Extra Rules\n\nSecondary rule.\n".to_string(),
        ),
    ];

    let actual = concatenate_rules(&files, Some(base));

    assert_eq!(
        actual,
        "\n\n<!-- Source: .ruler/AGENTS.md -->\n\n# Root Rules\n\nPrimary rule.\n\n\n\n<!-- Source: .ruler/nested/extra.md -->\n\n# Extra Rules\n\nSecondary rule.\n"
    );
}

#[test]
fn read_markdown_files_orders_primary_skips_skills_and_agents_by_default() {
    let tmp = tempdir().unwrap();
    let root = tmp.path();
    let ruler = root.join(".ruler");
    fs::create_dir_all(ruler.join("skills/demo")).unwrap();
    fs::create_dir_all(ruler.join("agents")).unwrap();
    fs::create_dir_all(ruler.join("z")).unwrap();
    fs::write(ruler.join("z/last.md"), "last").unwrap();
    fs::write(ruler.join("AGENTS.md"), "primary").unwrap();
    fs::write(ruler.join("alpha.md"), "alpha").unwrap();
    fs::write(ruler.join("skills/demo/SKILL.md"), "skill").unwrap();
    fs::write(ruler.join("agents/helper.md"), "agent").unwrap();
    fs::write(
        root.join("AGENTS.md"),
        "<!-- Generated by Ruler -->\n\n<!-- Source: .ruler/AGENTS.md -->\nold",
    )
    .unwrap();

    let fs = FsFileSystem::new();
    let files = fs.read_markdown_files(&ruler, false).unwrap();
    let rels: Vec<_> = files
        .iter()
        .map(|(path, _)| {
            path.strip_prefix(root)
                .unwrap()
                .to_string_lossy()
                .replace('\\', "/")
        })
        .collect();

    assert_eq!(
        rels,
        vec![".ruler/AGENTS.md", ".ruler/alpha.md", ".ruler/z/last.md"]
    );
}

#[test]
fn read_markdown_files_can_include_agents_and_root_agents_when_not_generated() {
    let tmp = tempdir().unwrap();
    let root = tmp.path();
    let ruler = root.join(".ruler");
    fs::create_dir_all(ruler.join("agents")).unwrap();
    fs::write(root.join("AGENTS.md"), "human root").unwrap();
    fs::write(ruler.join("instructions.md"), "legacy primary").unwrap();
    fs::write(ruler.join("agents/helper.md"), "agent").unwrap();

    let fs = FsFileSystem::new();
    let files = fs.read_markdown_files(&ruler, true).unwrap();
    let rels: Vec<_> = files
        .iter()
        .map(|(path, _)| {
            path.strip_prefix(root)
                .unwrap()
                .to_string_lossy()
                .replace('\\', "/")
        })
        .collect();

    assert_eq!(
        rels,
        vec![
            "AGENTS.md",
            ".ruler/instructions.md",
            ".ruler/agents/helper.md"
        ]
    );
}

#[test]
fn update_gitignore_replaces_managed_block_and_filters_existing_and_ruler_inputs() {
    let tmp = tempdir().unwrap();
    let root = tmp.path().join("ruler");
    fs::create_dir_all(&root).unwrap();
    fs::write(
        root.join(".gitignore"),
        "node_modules\n# START Ruler Generated Files\n/old.md\n# END Ruler Generated Files\n/dist\n",
    )
    .unwrap();

    let gitignore = GitignoreUpdater::new();
    gitignore
        .update_gitignore(
            &root,
            &[
                root.join("AGENTS.md"),
                PathBuf::from("ruler/AIDER_RULES.md"),
                PathBuf::from(".ruler/AGENTS.md"),
                PathBuf::from("dist"),
                PathBuf::from("AGENTS.md"),
            ],
            ".gitignore",
        )
        .unwrap();

    assert_eq!(
        fs::read_to_string(root.join(".gitignore")).unwrap(),
        "node_modules\n# START Ruler Generated Files\n/AGENTS.md\n/AIDER_RULES.md\n# END Ruler Generated Files\n/dist\n"
    );
}

#[test]
fn load_config_maps_toml_sections_and_resolves_output_paths() {
    let tmp = tempdir().unwrap();
    let root = tmp.path();
    fs::create_dir_all(root.join(".ruler")).unwrap();
    fs::write(
        root.join(".ruler/ruler.toml"),
        r#"
default_agents = ["agentsmd", "aider"]
nested = true

[mcp]
enabled = false
merge_strategy = "overwrite"

[gitignore]
enabled = true
local = true

[skills]
enabled = false

[agents]
enabled = true
include_in_rules = true

[agents.aider]
enabled = false
output_path = "rules/AIDER_RULES.md"
output_path_config = ".aider.conf.yml"

[agents.aider.mcp]
enabled = true
merge_strategy = "merge"
        "#,
    )
    .unwrap();

    let loader = TomlConfigLoader::new();
    let loaded = loader
        .load_config(root, None, Some(vec!["aider".to_string()]))
        .unwrap();

    assert_eq!(loaded.default_agents.unwrap(), vec!["agentsmd", "aider"]);
    assert_eq!(loaded.cli_agents.unwrap(), vec!["aider"]);
    assert!(loaded.nested);
    assert!(loaded.nested_defined);
    assert_eq!(loaded.mcp.unwrap().strategy, McpStrategy::Overwrite);
    assert_eq!(loaded.gitignore.unwrap().local, Some(true));
    assert_eq!(loaded.skills.unwrap().enabled, Some(false));
    assert_eq!(loaded.subagents.unwrap().include_in_rules, Some(true));

    let aider = loaded.agent_configs.get("aider").unwrap();
    assert_eq!(aider.enabled, Some(false));
    assert_eq!(
        aider.output_path.as_ref().unwrap(),
        &root.join("rules/AIDER_RULES.md")
    );
    assert_eq!(
        aider.output_path_config.as_ref().unwrap(),
        &root.join(".aider.conf.yml")
    );
    assert_eq!(aider.mcp.as_ref().unwrap().enabled, Some(true));
    assert_eq!(aider.mcp.as_ref().unwrap().strategy, McpStrategy::Merge);
}

#[test]
fn load_config_without_file_returns_empty_native_sections() {
    let tmp = tempdir().unwrap();

    let loader = TomlConfigLoader::new();
    let loaded = loader.load_config(tmp.path(), None, None).unwrap();

    assert!(loaded.default_agents.is_none());
    assert!(loaded.agent_configs.is_empty());
    let mcp = loaded.mcp.unwrap();
    assert_eq!(mcp.enabled, None);
    assert_eq!(mcp.strategy, McpStrategy::Merge);
    assert_eq!(loaded.gitignore.unwrap().enabled, None);
    assert_eq!(loaded.skills.unwrap().enabled, None);
    assert_eq!(loaded.subagents.unwrap().enabled, None);
    assert!(!loaded.nested);
    assert!(!loaded.nested_defined);
}

